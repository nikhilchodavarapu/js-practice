// Javascript program to perform Arithematic Operations

const x = 20
const y = 10

const sum = x + y;
const difference = x - y;
const product = x * y;
const quotient = x / y;
const remainder = x % y;
console.log(x, '+', y, '=', sum);
console.log(x, '-', y, '=', difference);
console.log(x, '*', y, '=', product);
console.log(x, '/', y, '=', quotient);
console.log(x, '%', y, '=', remainder);