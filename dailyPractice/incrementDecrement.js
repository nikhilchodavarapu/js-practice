console.log('Increment');
let x = 10;
console.log("x =", x);

let y = x++;
console.log("y = x++");
console.log("y = ", y);
console.log("x = ", x);


console.log()
x = 10;
console.log("x =", x);

y = ++x;
console.log("y = ++x");
