console.log("x = ", x);


//Decrement
console.log()
console.log('Decrement');
x = 10;
console.log("x =", x);

y = x--;
console.log("y = x--");
console.log("y = ", y);
console.log("x = ", x);


console.log()
x = 10;
console.log("x =", x);

