// Javascript program to perform decision making Statements.

let nameOfThePerson = "Shivaji"
let ageOfThePerson = 20

// if statement
if (ageOfThePerson > 18){
    console.log(nameOfThePerson, "is allowed to vote.")
}
console.log("If statement executed")

// if/else statement
if (ageOfThePerson < 18){
    console.log(nameOfThePerson, "isn't allowed to vote.")
} else{
    console.log(nameOfThePerson, "is allowed to vote.")
}
console.log("If/else statement executed")
